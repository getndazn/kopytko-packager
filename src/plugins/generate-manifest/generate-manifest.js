const args = require('../../env/args');
const fs = require('fs').promises;
const path = require('path');
const kopytkoConfig = require('../../env/kopytko-config');
const utils = require('../../utils');

module.exports = function generateManifest(rootDir) {
  const manifestBrsPath = path.join(rootDir, 'components/Manifest.const.brs');
  const manifestPath = path.join(rootDir, 'manifest');
  let manifestBrsContent = formatToBrsFile(kopytkoConfig.manifest);
  let manifestContent = formatToStringArray(kopytkoConfig.manifest).join('\n');

  if (args.forceHttp) {
    manifestBrsContent = manifestBrsContent.replace(/https:\/\//ig, 'http://');
    manifestContent = manifestContent.replace(/https:\/\//ig, 'http://');
  }

  return Promise.all([
    fs.writeFile(manifestBrsPath, manifestBrsContent, 'utf8'),
    fs.writeFile(manifestPath, manifestContent, 'utf8'),
  ]);
}

function formatToStringArray(settings) {
  return Object.entries(settings).map(([key, value]) => {
    // overwrites config with the command line arguments
    if (args[key] !== undefined) {
      value = args[key];
    }

    if (value === undefined || value === null) return '';

    const parsedValue = utils.isObject(value) ? formatToStringArray(value).join(';') : value.toString();

    return `${key}=${parsedValue}`;
  }).filter(entry => entry);
}

function formatToBrsFile(settings) {
  const brsObjectEntries = Object.entries(settings).map(([key, value]) => {
    // overwrites config with the command line arguments
    if (args[key] !== undefined) {
      value = args[key];
    }

    if (value === undefined || value === null) return '';

    if (utils.isObject(value)) return `    "${key}": ${formatBrsObject(value)},`;

    return `    "${key}": "${value.toString()}",`;
  }).filter(entry => entry).join('\n');

  return `
' This file is auto-generated by kopytko-packager. Do not edit manually.
function Manifest() as Object
  return {
${brsObjectEntries}
  }
end function
`
}

function formatBrsObject(obj) {
  const objEntries =  Object.entries(obj).map(([key, value]) => {
    // overwrites config with the command line arguments
    if (args[key] !== undefined) {
      value = args[key];
    }

    if (value === undefined || value === null) return '';

    const parsedValue = utils.isObject(value) ? formatBrsObject(value) : value.toString();

    return ` "${key}": "${parsedValue}"`;
  }).filter(entry => entry);

  return `{${objEntries.join(',')} }`;
}
